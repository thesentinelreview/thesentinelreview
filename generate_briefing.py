#!/usr/bin/env python3
"""
The Sentinel Review — Daily Morning Briefing Generator

Reads from feed.xml (generated by aggregate.py), selects top 5 stories,
and creates+sends a branded daily briefing email via the Buttondown API.

Environment variables required:
  BUTTONDOWN_API_KEY  - API key from Buttondown (stored as GitHub Secret)

Optional environment variables:
  BRIEFING_MODE       - "send" (default) or "draft" — draft creates without sending
"""

import feedparser
