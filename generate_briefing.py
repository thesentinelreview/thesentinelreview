# ============================================================
# CONFIGURATION
# ============================================================

BUTTONDOWN_API_BASE = "https://api.buttondown.com/v1"
FEED_XML_PATH = Path(__file__).parent / "feed.xml"
SITE_URL = "https://thesentinelreview.com"
BRIEFING_MODE = os.environ.get("BRIEFING_MODE", "send").lower()  # "send" or "draft"
TOP_STORIES_COUNT = 5
MAX_STORY_AGE_HOURS = 24  # Only include stories from past day

# Threat level can be made dynamic later; static for now
THREAT_LEVEL = "ELEVATED"
THREAT_DESCRIPTION = "SUBSTANTIAL"

# Category icons (mirrors aggregate.py)
CATEGORY_ICONS = {
    "Cyber": "🔐",
    "Indo-Pacific": "🌏",
    "Middle East": "🌍",
    "Europe / NATO": "🏴",
    "Nuclear": "⚛️",
    "Homeland": "🛡️",
    "Defense Policy": "🏛️",
    "Intelligence": "🕵️",
    "National Security": "📍",
}


# ============================================================
# READING FEED.XML
# ============================================================

def load_stories_from_feed():
    """Parse feed.xml (generated by aggregate.py) into story dicts."""
    if not FEED_XML_PATH.exists():
        print(f"❌ feed.xml not found at {FEED_XML_PATH}")
        sys.exit(1)

    parsed = feedparser.parse(str(FEED_XML_PATH))
    stories = []

    cutoff = datetime.now(timezone.utc) - timedelta(hours=MAX_STORY_AGE_HOURS)

    for entry in parsed.entries:
        # Title in feed.xml is "[icon] [headline] | [source]" — split it back out
        full_title = entry.get("title", "").strip()
        if " | " in full_title:
            title_part, source = full_title.rsplit(" | ", 1)
        else:
            title_part = full_title
            source = "The Sentinel Review"

        # Strip leading icon from title
        title = title_part
        for icon in CATEGORY_ICONS.values():
            if title.startswith(icon + " "):
                title = title[len(icon) + 1:]
                break

        # Parse pub date
        pub_date_str = entry.get("published") or entry.get("updated")
        try:
            pub_date = date_parser.parse(pub_date_str) if pub_date_str else datetime.now(timezone.utc)
        except Exception:
            pub_date = datetime.now(timezone.utc)
        if pub_date.tzinfo is None:
            pub_date = pub_date.replace(tzinfo=timezone.utc)

        # Filter to recent stories only
        if pub_date < cutoff:
            continue

        category = entry.get("category", "National Security")
        link = entry.get("link", "")
        summary = entry.get("summary", "") or entry.get("description", "")

        if not title or not link:
            continue

        stories.append({
            "title": title,
            "link": link,
            "source": source.strip(),
            "category": category,
            "icon": CATEGORY_ICONS.get(category, "📍"),
            "summary": summary,
            "pub_date": pub_date,
        })

    # Sort by recency (most recent first)
    stories.sort(key=lambda s: s["pub_date"], reverse=True)
    return stories


def select_briefing_stories(all_stories):
    """Pick top 5 stories with diversity across categories."""
    if len(all_stories) < TOP_STORIES_COUNT:
        print(f"⚠️  Only {len(all_stories)} fresh stories available (wanted {TOP_STORIES_COUNT})")
        return all_stories

    # Take the most recent story as hero
    selected = [all_stories[0]]
    used_categories = {all_stories[0]["category"]}

    # Fill remaining slots prioritizing category diversity
    for story in all_stories[1:]:
        if len(selected) >= TOP_STORIES_COUNT:
            break
        if story["category"] not in used_categories:
            selected.append(story)
            used_categories.add(story["category"])

    # If we still need more, fill with most recent regardless of category
    if len(selected) < TOP_STORIES_COUNT:
        for story in all_stories[1:]:
            if story not in selected:
                selected.append(story)
                if len(selected) >= TOP_STORIES_COUNT:
                    break

    return selected


