def render_briefing_subject(stories, briefing_date):
    """Generate the email subject line."""
    date_short = briefing_date.strftime("%b %d")
    if stories:
        # Use hero story headline, truncated
        hero_title = stories[0]["title"]
        if len(hero_title) > 60:
            hero_title = hero_title[:57].rsplit(" ", 1)[0] + "…"
        return f"Sentinel Briefing · {date_short} · {hero_title}"
    return f"Sentinel Briefing · {date_short}"


# ============================================================
# BUTTONDOWN API
# ============================================================

def post_to_buttondown(api_key, payload):
    """POST a new email to Buttondown's API."""
    url = f"{BUTTONDOWN_API_BASE}/emails"
    data = json.dumps(payload).encode("utf-8")

    headers = {
        "Authorization": f"Token {api_key}",
        "Content-Type": "application/json",
    }
    # Buttondown requires this header to confirm intent the first time
    # an API key is used to send a live (non-draft) email.
    if payload.get("status") == "about_to_send":
        headers["X-Buttondown-Live-Dangerously"] = "true"

    req = urllib.request.Request(
        url,
        data=data,
        headers=headers,
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=30) as resp:
            return resp.status, json.loads(resp.read().decode("utf-8"))
    except urllib.error.HTTPError as e:
        body = e.read().decode("utf-8", errors="replace")
        return e.code, body
    except Exception as e:
        return 0, str(e)


# ============================================================
# HELPERS
# ============================================================

def esc(text):
    return html.escape(str(text))


def truncate(text, max_chars):
    if not text:
        return ""
    text = text.strip()
    if len(text) <= max_chars:
        return text
    return text[:max_chars].rsplit(" ", 1)[0] + "…"


# ============================================================
# MAIN
# ============================================================

def main():
    print("🦉 The Sentinel Review — Briefing Generator\n")

    api_key = os.environ.get("BUTTONDOWN_API_KEY")
    if not api_key:
        print("❌ BUTTONDOWN_API_KEY not set in environment.")
        sys.exit(1)

    # Load stories
    print("Loading stories from feed.xml...")
    all_stories = load_stories_from_feed()
    print(f"  Found {len(all_stories)} fresh stories (last {MAX_STORY_AGE_HOURS}h)")

    if len(all_stories) < 3:
        print(f"❌ Only {len(all_stories)} stories available — refusing to send a thin briefing.")
        sys.exit(1)

    # Select briefing stories
    selected = select_briefing_stories(all_stories)
    print(f"  Selected {len(selected)} stories for briefing:")
    for i, s in enumerate(selected, 1):
        print(f"    {i}. [{s['category']}] {s['title'][:70]}")

    # Render content
    briefing_date = datetime.now(timezone.utc)
    subject = render_briefing_subject(selected, briefing_date)
    body_html = render_briefing_html(selected, briefing_date)

    print(f"\n  Subject: {subject}")
    print(f"  Body length: {len(body_html)} chars")

    # Build payload
    # Buttondown uses 'status' to control whether email is sent or kept as draft
    # "about_to_send" = send immediately
    # "draft"         = save as draft for manual review
    status = "about_to_send" if BRIEFING_MODE == "send" else "draft"

    payload = {
        "subject": subject,
        "body": body_html,
        "email_type": "public",  # appears in archive
        "status": status,
    }

    print(f"\n  Mode: {BRIEFING_MODE.upper()} (status={status})")
    print("  Posting to Buttondown API...")

    code, response = post_to_buttondown(api_key, payload)

    if code in (200, 201):
        email_id = response.get("id", "(unknown)") if isinstance(response, dict) else "(unknown)"
        print(f"\n✅ Briefing created successfully (id={email_id})")
        if BRIEFING_MODE == "send":
            print("   → Email will be sent to all subscribers within minutes.")
        else:
            print("   → Saved as draft. Review and send manually from Buttondown.")
    else:
        print(f"\n❌ Buttondown API returned status {code}")
        print(f"   Response: {response}")
        sys.exit(1)


if __name__ == "__main__":
    main()
